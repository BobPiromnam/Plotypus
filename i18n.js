  function normalizeLocale(locale) {
    return locale === "fr" ? "fr" : "en";
  }

  function translate(locale, key, params) {
    const lang = normalizeLocale(locale);
    let value = dictionaries[lang][key] || dictionaries.en[key] || key;
    Object.entries(params || {}).forEach(([name, replacement]) => {
      value = value.replace(new RegExp(`\\{${name}\\}`, "g"), String(replacement));
    });
    return value;
  }

  function setNodeText(node, value) {
    if (!node) return;
    node.textContent = value;
  }

  function getNodeParams(node) {
    if (!node || !node.dataset || !node.dataset.i18nParams) return {};
    try {
      return JSON.parse(node.dataset.i18nParams);
    } catch (_error) {
      return {};
    }
  }

  function applyStaticTranslations(locale, root) {
    const scope = root || global.document;
    if (!scope || !scope.querySelectorAll) return;
    const lang = normalizeLocale(locale);
    scope.querySelectorAll("[data-i18n]").forEach(node => {
      setNodeText(node, translate(lang, node.dataset.i18n, getNodeParams(node)));
    });
    scope.querySelectorAll("[data-i18n-title]").forEach(node => {
      node.setAttribute("title", translate(lang, node.dataset.i18nTitle, getNodeParams(node)));
    });
    scope.querySelectorAll("[data-i18n-aria-label]").forEach(node => {
      node.setAttribute("aria-label", translate(lang, node.dataset.i18nAriaLabel, getNodeParams(node)));
    });
  }

  global.PLOTYPUS_I18N = Object.freeze({
    applyStaticTranslations,
    dictionaries,
    normalizeLocale,
    t: translate
  });
